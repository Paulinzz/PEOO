# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. 
# Calcule e mostre o total do seu salário no referido mês.

salario_phr = float(input("Digite quanto você ganha por hora: "))
números_de_horas = int(input("Digite a quantidade de horas que você trabalha: "))

print(f"O salário segundo a informada que você inseriu é de: {salario_phr* números_de_horas} $")
